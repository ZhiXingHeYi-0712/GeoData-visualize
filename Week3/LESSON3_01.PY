"""
开始定义一个函数，函数的名字命名要有一点意义哟
例如，我定义了一个函数，通过函数名就知道这是计算点到点的距离计算函数
函数有四个输入参数，分别是第一个点的坐标x0,y0,第二个点的坐标：x1,y1
"""


def pointToPointDist(x0,y0,x1,y1):
    d = (x0-x1)**2 + (y0-y1)**2
    dist = d**0.5
    #把计算“告诉”别人，用的是return
    return(dist)
#下面开始调用这个函数了
#首先要定义四个变量（对应的两个点的x，y坐标对
    
#河南省的X，Y坐标   
x_hn = 12648062.2
y_hn = 4015701.14

#山东省的X，Y坐标
x_sd = 13155816.9
y_sd = 4351795.1
#安徽省的X，Y坐标
x_ah = 13049690.2
y_ah = 3743472.8
#计算河南到安徽的距离
d_hn_ah = pointToPointDist(x_hn,y_hn,x_ah,y_ah)
"""
请你继续写代码，输出如下字符串
河南到安徽的距离是 xxx 公里
注意，将结果单位从米转换为公里，然后将xxx替换成你计算得到的结果
"""
#在下面空白行补充代码1，按照要求输出计算结果
print('河南到安徽的距离是{}公里'.format(int(d_hn_ah / 1000)))

"""
请你继续补充代码，计算河南到山东的距离，可以保留一位小数，然后输出相应的字符串
"""
#在下面空白行补充代码2,计算河南到山东的距离
d_hn_sd = pointToPointDist(x_hn, y_hn, x_sd, y_sd)

#在下面空白行补充代码3，按照要求输出计算结果
print('河南到山东的距离是{:.1f}公里'.format(d_hn_sd / 1000))
